DECLARE user_cursor CURSOR LOCAL FAST_FORWARD FOR
    SELECT RequestedFor FROM #Users;

OPEN user_cursor;
FETCH NEXT FROM user_cursor INTO @RequestedFor;

WHILE @@FETCH_STATUS = 0
BEGIN
    -- Get next RequestCode from proc (no duplicate with seed or app-created requests)
    DELETE FROM #ProcResult;
    INSERT INTO #ProcResult (RequestCode, DomainLoV)
    EXEC dbo.GeneratePermissionRequestCode @WorkspaceId = @WorkspaceId;

    SELECT @RequestCode = RequestCode FROM #ProcResult;
    IF @RequestCode IS NULL OR LTRIM(RTRIM(@RequestCode)) = N''
    BEGIN
        RAISERROR('GeneratePermissionRequestCode returned empty RequestCode for WorkspaceId %d.', 16, 1, @WorkspaceId);
        CLOSE user_cursor;
        DEALLOCATE user_cursor;
        DROP TABLE #ProcResult;
        DROP TABLE #Users;
        RETURN;
    END

    -- 1) PermissionRequests
    INSERT INTO dbo.PermissionRequests
        (RequestCode, RequestedFor, RequestedBy, LMApprover, RequestStatus, RequestReason, WorkspaceId, CreatedAt, CreatedBy, UpdatedAt, UpdatedBy)
    VALUES
        (@RequestCode, @RequestedFor, @RequestedFor, @RequestedFor, 3, N'MSS level access for DFI', @WorkspaceId, GETDATE(), @CreatedBy, GETDATE(), @CreatedBy);
    SET @PermissionRequestId = SCOPE_IDENTITY();

    -- 2) PermissionHeaders (RLS, Approved)
    INSERT INTO dbo.PermissionHeaders
        (PermissionRequestId, PermissionType, ApprovalStatus, Approvers, ApprovedBy, ApprovedAt, ApproveNote,
         RejectedBy, RejectedAt, RejectNote, RevokedBy, RevokedAt, RevokeNote, CreatedAt, CreatedBy, UpdatedAt, UpdatedBy)
    VALUES
        (@PermissionRequestId, 1, 2, N'Terry.Newell@dentsu.com', N'Terry.Newell@dentsu.com', GETDATE(), N'Approved',
         NULL, NULL, NULL, NULL, NULL, NULL, GETDATE(), @CreatedBy, GETDATE(), @CreatedBy);
    SET @PermissionHeaderId = SCOPE_IDENTITY();

    -- 3) RLSPermissions (DFI-Default, FUM)
    INSERT INTO dbo.RLSPermissions
        (PermissionHeaderId, SecurityModelId, SecurityTypeLoVId, AdditionalDetailsJSON, CreatedAt, CreatedBy, UpdatedAt, UpdatedBy)
    VALUES
        (@PermissionHeaderId, @SecurityModelId, @SecurityTypeLoVId, N'{}', GETDATE(), @CreatedBy, GETDATE(), @CreatedBy);
    SET @RLSPermissionsId = SCOPE_IDENTITY();

    -- 4) RLSPermissionFUMDetails — MSS level scope
    INSERT INTO dbo.RLSPermissionFUMDetails
        (RLSPermissionsId, EntityKey, EntityHierarchy, CountryKey, CountryHierarchy, ClientKey, ClientHierarchy,
         MSSKey, MSSHierarchy, ProfitCenterKey, ProfitCenterHierarchy, CreatedAt, CreatedBy, UpdatedAt, UpdatedBy)
    VALUES
        (@RLSPermissionsId, N'Global', N'Global', NULL, NULL, N'All Clients', N'All Clients',
         N'Overall', N'Overall', N'BR_TOTAL', N'BPCBrand', GETDATE(), @CreatedBy, GETDATE(), @CreatedBy);

    FETCH NEXT FROM user_cursor INTO @RequestedFor;
END

CLOSE user_cursor;
DEALLOCATE user_cursor;
DROP TABLE #ProcResult;
DROP TABLE #Users;

PRINT 'Done. 18 new DFI permission requests for MSS-level access (RequestCodes from proc), all approved.';

GO
