/*
================================================================================
DFI: New permission requests for MSS-level access (approved)
================================================================================
Creates NEW permission requests for 18 users. Each request grants RLS scope at
MSS level (Global, All Clients, MSS Overall, BR_TOTAL) and is created as APPROVED.
Does NOT touch RLSFUMApprovers (approvers).

RequestCode is generated by dbo.GeneratePermissionRequestCode (same as the app),
so no duplicate codes with seed data or other requests. One request per user;
full RLS chain: PermissionRequests -> PermissionHeaders -> RLSPermissions ->
RLSPermissionFUMDetails.

Requires: dbo.GeneratePermissionRequestCode exists and returns RequestCode (and
optionally DomainLoV). Run after 0-Global and 1-DFI. Safe to run multiple times
(creates 18 new requests each run; skip if these users already have MSS access).
================================================================================
*/
SET NOCOUNT ON;

DECLARE @WorkspaceId INT;
DECLARE @SecurityModelId INT;
DECLARE @SecurityTypeLoVId INT;
DECLARE @CreatedBy NVARCHAR(255) = N'sakurahelp@dentsu.com';
DECLARE @RequestCode NVARCHAR(50);
DECLARE @RequestedFor NVARCHAR(255);
DECLARE @PermissionRequestId INT;
DECLARE @PermissionHeaderId INT;
DECLARE @RLSPermissionsId INT;

SELECT @WorkspaceId = Id FROM dbo.Workspaces WHERE WorkspaceCode = N'DFI' AND ISNULL(ValidTo, '9999-12-31') >= CAST(GETDATE() AS DATE);
SELECT @SecurityModelId = Id FROM dbo.WorkspaceSecurityModels WHERE SecurityModelCode = N'DFI-Default' AND ISNULL(ValidTo, '9999-12-31') >= CAST(GETDATE() AS DATE);
SELECT @SecurityTypeLoVId = Id FROM dbo.LoVs WHERE LoVType = N'SecurityType' AND LoVValue = N'FUM' AND ISNULL(ValidTo, '9999-12-31') >= CAST(GETDATE() AS DATE);

IF @WorkspaceId IS NULL OR @SecurityModelId IS NULL OR @SecurityTypeLoVId IS NULL
BEGIN
    RAISERROR('Missing DFI WorkspaceId, DFI-Default SecurityModelId, or FUM SecurityTypeLoVId. Run 0-Global first.', 16, 1);
    RETURN;
END

-- Table to capture proc output (column names must match what GeneratePermissionRequestCode returns)
CREATE TABLE #ProcResult (RequestCode NVARCHAR(50), DomainLoV NVARCHAR(50) NULL);

-- List of users to grant MSS-level access
CREATE TABLE #Users (RequestedFor NVARCHAR(255) NOT NULL);
